-- 'Can grow' function

local random = math.random

local function can_grow(pos)
	local node_under = minetest.get_node_or_nil({x = pos.x, y = pos.y - 1, z = pos.z})
	if not node_under then
		return false
	end
	local name_under = node_under.name
	local is_soil = minetest.get_item_group(name_under, "soil")
	if is_soil == 0 then
		return false
	end
	local ll = minetest.get_node_light(pos)
	if not ll or ll < 13 then -- Minimum light level for growth
		return false          -- matches grass, wheat and cotton
	end
	return true
end


-- Sapling ABM

minetest.register_abm({
	nodenames = {"tree:apple_tree_sapling", "tree:junglesapling",
		"tree:pine_sapling", "tree:acacia_sapling"},
	interval = 10,
	chance = 50,
	action = function(pos, node)
		if not can_grow(pos) then
			return
		end

		local mapgen = minetest.get_mapgen_params().mgname
		if node.name == "tree:apple_tree_sapling" then
			minetest.log("action", "A sapling grows into a tree at "..
				minetest.pos_to_string(pos))
			if mapgen == "v6" then
				tree.grow_tree(pos, random(1, 4) == 1)
			else
				tree.grow_new_apple_tree(pos)
			end
		elseif node.name == "tree:junglesapling" then
			minetest.log("action", "A jungle sapling grows into a tree at "..
				minetest.pos_to_string(pos))
			if mapgen == "v6" then
				tree.grow_jungle_tree(pos)
			else
				tree.grow_new_jungle_tree(pos)
			end
		elseif node.name == "tree:pine_sapling" then
			minetest.log("action", "A pine sapling grows into a tree at "..
				minetest.pos_to_string(pos))
			if mapgen == "v6" then
				tree.grow_pine_tree(pos)
			else
				tree.grow_new_pine_tree(pos)
			end
		elseif node.name == "tree:acacia_sapling" then
			minetest.log("action", "An acacia sapling grows into a tree at "..
				minetest.pos_to_string(pos))
			tree.grow_new_acacia_tree(pos)
		end
	end
})


